package com.tropicalbastos.boids.animation;

public class Sheet {

    private int x;
    private int y;
    private int width;
    private int height;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public void setX(int val) {
        x = val;
    }

    public void setY(int val) {
        y = val;
    }

    public void setWidth(int val) {
        width = val;
    }

    public void setHeight(int val) {
        height = val;
    }

}